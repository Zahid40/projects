function calculateAgeDetails(dob_user) {

    var dob = new Date(dob_user);

    var dobYear = dob.getYear();
    var dobMonth = dob.getMonth();
    var dobDate = dob.getDate();

    //get the current date from the system  
    var now = new Date();
    //extract the year, month, and date from current date  
    var currentYear = now.getYear();
    var currentMonth = now.getMonth();
    var currentDate = now.getDate();
    var currentHours = now.getHours();
    var currentMinutes = now.getMinutes();

    //get years  
    var yearAge = currentYear - dobYear;

    //get months  
    if (currentMonth >= dobMonth)
        //get months when current month is greater  
        var monthAge = currentMonth - dobMonth;
    else {
        yearAge--;
        var monthAge = 12 + currentMonth - dobMonth;
    }
    //upcoming month


    //get days  
    if (currentDate >= dobDate)
        //get days when the current date is greater  
        var dateAge = currentDate - dobDate;
    else {
        monthAge--;
        var dateAge = 31 + currentDate - dobDate;

        if (monthAge < 0) {
            monthAge = 11;
            yearAge--;
        }
    }

    //get hours
    var hourAge;
    if (currentHours >= dob.getHours())
        hourAge = currentHours - dob.getHours();
    else {
        dateAge--;
        hourAge = 24 + currentHours - dob.getHours();

        if (dateAge < 0) {
            dateAge = 30;  // Assuming 30 days in a month
            monthAge--;
        }
    }

    //get minutes
    var minuteAge;
    if (currentMinutes >= dob.getMinutes())
        minuteAge = currentMinutes - dob.getMinutes();
    else {
        hourAge--;
        minuteAge = 60 + currentMinutes - dob.getMinutes();

        if (hourAge < 0) {
            hourAge = 23;
            dateAge--;
            if (dateAge < 0) {
                dateAge = 30;
                monthAge--;
            }
        }
    }

    return {
        years: yearAge,
        months: monthAge,
        days: dateAge,
        hours: hourAge,
        minutes: minuteAge
    };
}



